'''
    Module for calculator functions
'''


class Calc:
    '''
        Calc class will support these requirements
    '''

    def add(self, *s):
        '''
            Adds numbers a and b together
        '''
        return sum(s)

    def sub(self, first_parm, second_parm):
        '''
            Subtract b from a
        '''
        return first_parm - second_parm
